async def seed_user_categories(db, user_id) -> int:
    """Сеет дефолтные категории, если у пользователя их ещё нет. Идемпотентно.
    Возвращает число созданных категорий (0 — если уже были)."""
    n = await db.fetchval("SELECT count(*) FROM categories WHERE user_id=$1", user_id)
    if n and int(n) > 0:
        return 0
    await db.executemany("""
        INSERT INTO categories (user_id, group_name, category, subcategory, character, expense_type, role)
        VALUES ($1, $2, $3, $4, $5, $6, $7)
    """, [(user_id, *row) for row in DEFAULT_CATEGORIES])
    return len(DEFAULT_CATEGORIES)

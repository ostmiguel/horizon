-- ── Убрать рудиментный общий пул «Обязательства» ─────────────────────────────
-- В новой модели каждое обязательство = свой счёт-Пассив (создаётся при добавлении
-- кредита/долга). Общий пул больше не нужен. Деактивируем ПУСТЫЕ пулы (без операций)
-- — они висят в Справочниках зря. Пулы с историей операций (не мигрированные
-- вручную) НЕ трогаем: их владелец должен мигрировать кнопкой. Идемпотентно.

UPDATE accounts a
SET is_active = false
WHERE a.name = 'Обязательства'
  AND a.is_active = true
  AND NOT EXISTS (
    SELECT 1 FROM transactions t
    WHERE t.user_id = a.user_id
      AND (t.account_to = 'Обязательства' OR t.account_from = 'Обязательства')
  );

-- Каждое обязательство = отдельный счёт-Пассив. Кредит (loans) линкуется со
-- своим счётом через account_name; тело материализуется в этот счёт (не в пул).
-- Аддитивно и безопасно: пока account_name пуст — материализация падает на
-- старый пул «Обязательства» (обратно совместимо). Заполнение account_name и
-- создание счетов — отдельным осознанным шагом (миграция данных владельца).

ALTER TABLE loans ADD COLUMN IF NOT EXISTS account_name TEXT;
